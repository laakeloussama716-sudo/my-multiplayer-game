const express = require('express');
const app = express();
const http = require('http').createServer(app);
const io = require('socket.io')(http, {
    cors: { origin: "*" }
});

const PORT = process.env.PORT || 3000;

// ???? ????? ?????? ????????
app.use(express.static(__dirname));

// ????? ??? ????? ?????????
let rooms = {};
let queues = {
    '1v1': [],
    'BR': [],
    'CoOp': []
};

io.on('connection', (socket) => {
    console.log(`???? ???? ????: ${socket.id}`);

    // ??????? ??? ????? ?????? ?????? ?? ????????
    socket.on('login', (data) => {
        console.log(`??? ???? ??????: ${data.email}`);
        // ????? ??????? ?????? ?????? ?? ?????? ??? ???????? ???????? ?? Rozixo
        socket.emit('loginResponse', { success: true, gold: 526, stage: 6, nickname: "Rozixo" });
    });

    // ???? ????? 1 ?? 1
    socket.on('join1v1Queue', (data) => {
        handleQueueJoin(socket, '1v1', data);
    });

    // ???? ????? Battle Royale
    socket.on('joinBRQueue', (data) => {
        handleQueueJoin(socket, 'BR', data);
    });

    // ???? ????? ????? ????????
    socket.on('joinCoOpQueue', (data) => {
        handleQueueJoin(socket, 'CoOp', data);
    });

    // ??????? ???? ??????? ????????
    socket.on('playerInput', (inputData) => {
        let roomId = socket.roomId;
        if (roomId && rooms[roomId]) {
            let player = rooms[roomId].players[socket.id];
            if (player) {
                player.x = inputData.x ?? player.x;
                player.y = inputData.y ?? player.y;
                player.angle = inputData.angle ?? player.angle;
                player.isMoving = inputData.isMoving ?? player.isMoving;
            }
        }
    });

    // ??????? ??? ????? ?????
    socket.on('fireBullet', (bulletData) => {
        let roomId = socket.roomId;
        if (roomId && rooms[roomId]) {
            rooms[roomId].bullets.push({
                id: Math.random().toString(36).substr(2, 9),
                ownerId: socket.id,
                x: bulletData.x,
                y: bulletData.y,
                angle: bulletData.angle,
                speed: 10
            });
        }
    });

    // ???? ?????? ?? ???????
    socket.on('leaveQueue', () => {
        removePlayerFromAllQueues(socket.id);
    });

    // ?????? ?????? ?? ???????
    socket.on('disconnect', () => {
        console.log(`???? ????? ?? ??????: ${socket.id}`);
        removePlayerFromAllQueues(socket.id);
        
        let roomId = socket.roomId;
        if (roomId && rooms[roomId]) {
            delete rooms[roomId].players[socket.id];
            socket.to(roomId).emit('playerLeft', socket.id);
            if (Object.keys(rooms[roomId].players).length === 0) {
                clearInterval(rooms[roomId].interval);
                delete rooms[roomId];
            }
        }
    });
});

// ???? ?????? ?????? ???????? ?????? ?????? ???????
function handleQueueJoin(socket, mode, data) {
    removePlayerFromAllQueues(socket.id);
    queues[mode].push({ socket: socket, nickname: data.nickname, email: data.email });
    
    console.log(`???? ?? ????? ???????? [${mode}]. ????? ??????: ${queues[mode].length}`);

    // ????? ????? ???????: ?????? ???? ????? ??? ???? ?????? ????? ?? ????
    if (queues[mode].length >= 2) {
        let player1 = queues[mode].shift();
        let player2 = queues[mode].shift();

        let roomId = `room_${mode}_${Date.now()}`;
        
        rooms[roomId] = {
            mode: mode,
            players: {},
            bullets: []
        };

        // ??? ?????? ???????? ?? ??? ??????
        setupPlayerInRoom(player1, roomId);
        setupPlayerInRoom(player2, roomId);

        // ????? ??????? ????? ???? ?????? ????????
        io.to(roomId).emit('matchJoined', { roomId: roomId, mode: mode });
        
        // ????? ???? ??????? ??????? ?????? (Game Loop)
        startGameLoop(roomId);
    }
}

function setupPlayerInRoom(playerData, roomId) {
    let socket = playerData.socket;
    socket.join(roomId);
    socket.roomId = roomId;
    
    rooms[roomId].players[socket.id] = {
        id: socket.id,
        nickname: playerData.nickname,
        email: playerData.email,
        x: 400,
        y: 300,
        angle: 0,
        isMoving: false
    };
}

function startGameLoop(roomId) {
    rooms[roomId].interval = setInterval(() => {
        if (!rooms[roomId]) return;

        // ????? ???? ?????? ?? ???????
        let bullets = rooms[roomId].bullets;
        bullets.forEach((b, index) => {
            b.x += Math.cos(b.angle) * b.speed;
            b.y += Math.sin(b.angle) * b.speed;
            
            // ??? ?????? ?????? ?? ?????? ??????????
            if (b.x < 0 || b.x > 2000 || b.y < 0 || b.y > 2000) {
                bullets.splice(index, 1);
            }
        });

        // ??????? ???????: ????? ????????? ???? ???? ????? ??? ???????? ???????? ???????
        io.to(roomId).emit('gameUpdate', {
            players: Object.values(rooms[roomId].players),
            bullets: rooms[roomId].bullets
        });
    }, 1000 / 30); // 30 ????? ?? ??????? ????? ???? ??? Lag
}

function removePlayerFromAllQueues(socketId) {
    for (let mode in queues) {
        queues[mode] = queues[mode].filter(p => p.socket.id !== socketId);
    }
}

http.listen(PORT, () => {
    console.log(`????? ?????? ???? ?????? ??? ?????? ${PORT}`);
});